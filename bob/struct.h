#ifndef _STRUCT_H_
#define _STRUCT_H_

typedef struct MEMBER MEMBER;
typedef struct TEAM TEAM;

struct MEMBER
{
	char name[20];
	int prev_team; //직전 조가 없었던 경우 -1로 취함
	char gender; //f:female, m:male
	int Pcode; // 개인 고유 번호 (사용여부 x)
};

struct TEAM
{
	//TEAM 번호는 +1해서 출력
	MEMBER *member; //member 추가할 배열
	int* set[5]; //중복 checking할 배열
};

#endif //!_STRUCT_H_